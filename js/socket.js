var socket = io("192.168.11.57:6969");

socket.on("timeout_blue", (data) => {
    if (data === "blue") {
        Co.redWin = true;
        setTimeout(function () {
            Co.game.state.start('win');
        }, 1200);
    }
});

socket.on("timeout_red", (data) => {
    if (data === "red") {
        Co.blueWin = true;
        setTimeout(function () {
            Co.game.state.start('win');
        }, 1200);
    }
});
